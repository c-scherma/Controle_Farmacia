package aux;

public class Medicamento {
    private String nome;
    private float valor;
    
    //-------------------
    // Getters e Setters
    //-------------------

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public float getValor() {
        return valor;
    }

    public void setValor(float valor) {
        this.valor = valor;
    }
    
    
}
